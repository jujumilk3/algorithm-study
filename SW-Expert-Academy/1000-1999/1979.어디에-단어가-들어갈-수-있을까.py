case_count = int(input())

for i in range(1, case_count + 1):
    size, length = list(map(int, input().split()))
    matrix = [list(map(int, input().split())) for _ in range(size)]
    found = 0

    for j in range(size):
        cnt = 0
        for k in range(size):
            if matrix[j][k] == 0:
                if cnt == length:
                    found += 1
                cnt = 0
                continue
            cnt += 1

        if cnt == length:
            found += 1

    for j in range(size):
        cnt = 0
        for k in range(size):
            if matrix[k][j] == 0:
                if cnt == length:
                    found += 1
                cnt = 0
                continue
            cnt += 1

        if cnt == length:
            found += 1
    print('#{} {}'.format(i, found))

